// District select ni to'ldirish (admin va boshqa sahifalar uchun)
function fillDistrictSelect(selectId, addAll = false) {
  const sel = document.getElementById(selectId);
  if (!sel) return;
  const t = translations[getLang()];
  sel.innerHTML = '';
  if (addAll) {
    const opt = document.createElement('option');
    opt.value = 'all'; opt.textContent = t.filter_all;
    sel.appendChild(opt);
  } else {
    const opt = document.createElement('option');
    opt.value = ''; opt.textContent = t.district_ph || '—';
    sel.appendChild(opt);
  }
  DISTRICTS.forEach(d => {
    const opt = document.createElement('option');
    opt.value = d.value;
    opt.textContent = t[d.key] || d.value;
    sel.appendChild(opt);
  });
}
